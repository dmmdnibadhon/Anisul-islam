for (var i = 1; i <= 100; i++){
    document.write(" " + i);
}
document.write(" For loop the end.");

var x = 1;
while (x <= 100) {
    document.write(" " + x);
    x++;
}
document.write(" While loop the end.");

var y = 1;
do {
    document.write(" " + y);
    y++;
} while (y <= 100);
document.write(" Do while loop the end");